_BDD_CHUNK_SIZE = 10
_BDD_BATCH_MAX_TOKENS = 4096


def _chunk_list(lst: List, size: int) -> List[List]:
    return [lst[i:i + size] for i in range(0, len(lst), size)]


class BDDGenerator:
    """Fonksiyonel gereksinimlerden Gherkin formatında BDD senaryoları üretir.

    LLM (Gemini) kullanarak her FUNCTIONAL gereksinim için en az 1 happy path
    ve 1 negative senaryo içeren Gherkin metin blokları üretir.
    Gereksinimler chunk'lara bölünerek paralel LLM çağrılarıyla işlenir.

    Args:
        llm_client: Dışarıdan enjekte edilebilir LLM istemcisi (test/DI için).
                    Verilmezse generate() çağrısında otomatik oluşturulur.
    """

    def __init__(self, llm_client: Optional[LLMClient] = None) -> None:
        self._llm_client = llm_client

    def _get_client(self) -> LLMClient:
        if self._llm_client is None:
            # BDD üretimi yapısal bir işlem olduğundan f/p açısından en hızlı model olan Gemini 2.5 Flash tercih edildi
            self._llm_client = LLMClient(model_name="google/gemini-2.5-flash", max_output_tokens=_BDD_BATCH_MAX_TOKENS)
        return self._llm_client

    def _generate_single_bdd(self, req: Requirement) -> List[str]:
        """Tek bir gereksinim için LLM'e çağrı yapar ve Gherkin blokları döner.

        Args:
            req: BDD senaryosuna dönüştürülecek Requirement.

        Returns:
            list[str]: [happy_path_gherkin, negative_gherkin] blokları.

        Raises:
            LLMClientError: LLM çağrısı başarısız olursa.
        """
        system_prompt = build_bdd_generation_system_prompt()
        user_prompt = build_bdd_generation_user_prompt(req)

        client = self._get_client()

        try:
            response = client.chat(
                system_prompt=system_prompt,
                user_prompt=user_prompt,
                metadata={
                    "module": "bdd_generator",
                    "action": "generate_bdd_scenarios",
                    "req_id": req.id,
                },
            )
        except LLMClientError:
            _log.exception("LLM çağrısı başarısız (BDD üretimi) | req_id={}", req.id)
            raise

        try:
            payload = extract_json_object(response.content)
        except ValueError as exc:
            _log.error(
                "LLM çıktısı JSON olarak çözülemedi | req_id={} preview={}",
                req.id,
                response.content[:300] if response.content else "",
            )
            raise LLMClientError(f"BDD çıktısı işlenemedi: {exc}") from exc

        return _normalize_llm_payload(payload, req.id)

    def _process_chunk(self, chunk: List[Requirement]) -> List[str]:
        """Tek bir chunk için LLM çağrısı yapar ve Gherkin blokları döner."""
        system_prompt = build_bdd_generation_batch_system_prompt()
        user_prompt = build_bdd_generation_batch_user_prompt(chunk)
        client = self._get_client()

        response = client.chat(
            system_prompt=system_prompt,
            user_prompt=user_prompt,
            metadata={"module": "bdd_generator", "action": "batch_generate_bdd"},
        )

        raw = extract_json_object(response.content)
        items = raw if isinstance(raw, list) else (list(raw.values())[0] if isinstance(raw, dict) and raw else [])

        req_index = {r.id: r for r in chunk}
        scenarios: List[str] = []
        returned_ids: set = set()

        for item in items:
            if not isinstance(item, dict):
                continue
            req_id = item.get("req_id", "")
            returned_ids.add(req_id)
            scenarios.extend(_normalize_llm_payload(item, req_id))

        for req in chunk:
            if req.id not in returned_ids:
                _log.warning("Chunk yanıtında req_id eksik, fallback | req_id={}", req.id)
                scenarios.extend(_make_fallback_gherkin(req_index[req.id]))

        return scenarios

    def _generate_batch_bdd(self, reqs: List[Requirement]) -> List[str]:
        """Gereksinimleri chunk'lara bölerek paralel LLM çağrılarıyla Gherkin bloklarına dönüştürür.

        Args:
            reqs: BDD senaryosuna dönüştürülecek Requirement listesi.

        Returns:
            list[str]: Gherkin formatında senaryo blokları (giriş sırasıyla).
        """
        chunks = _chunk_list(reqs, _BDD_CHUNK_SIZE)

        if len(chunks) == 1:
            return self._process_chunk(chunks[0])

        _log.info("BDD üretimi {} chunk'a bölündü, paralel işleniyor", len(chunks))
        with concurrent.futures.ThreadPoolExecutor(max_workers=len(chunks)) as executor:
            futures = [executor.submit(self._process_chunk, chunk) for chunk in chunks]
            results: List[str] = []
            for future in futures:
                try:
                    results.extend(future.result())
                except (LLMClientError, ValueError) as exc:
                    chunk_idx = futures.index(future)
                    _log.warning("BDD chunk {} başarısız, fallback | hata={}", chunk_idx, exc)
                    for req in chunks[chunk_idx]:
                        results.extend(_make_fallback_gherkin(req))
        return results

    def generate(self, report: AnalysisReport) -> List[str]:
        """AnalysisReport içindeki FUNCTIONAL gereksinimleri Gherkin BDD senaryolarına dönüştürür.

        Tüm gereksinimler tek bir toplu LLM çağrısıyla işlenir.
        LLM hatası durumunda tüm gereksinimler için fallback Gherkin metni döner.

        Args:
            report: NLP ve LLM analizleri tamamlanmış rapor nesnesi.

        Returns:
            list[str]: Her biri geçerli Gherkin formatında Feature/Scenario/Given/When/Then
                       bloklarından oluşan string listesi.
                       Her FUNCTIONAL gereksinim için minimum 1 happy + 1 negative senaryo.
        """
        functional_reqs = [
            req
            for req in report.parsed_doc.requirements
            if req.req_type == "FUNCTIONAL"
        ]

        if not functional_reqs:
            _log.info(
                "BDD üretimi atlandı: FUNCTIONAL gereksinim yok | toplam_gereksinim={}",
                len(report.parsed_doc.requirements),
            )
            return []

        _log.info(
            "BDD toplu üretimi başlatıldı | functional_count={}",
            len(functional_reqs),
        )

        try:
            scenarios = self._generate_batch_bdd(functional_reqs)
        except (LLMClientError, ValueError) as exc:
            _log.warning(
                "Toplu BDD üretimi başarısız, tüm req'ler için fallback | hata={}",
                exc,
            )
            scenarios = []
            for req in functional_reqs:
                scenarios.extend(_make_fallback_gherkin(req))

        _log.info("BDD üretimi tamamlandı | toplam_blok={}", len(scenarios))
        return scenarios

    def write_feature_file(
        self,
        scenarios: List[str],
        output_path: Optional[Path] = None,
        in_memory: bool = False,
    ) -> Path | io.BytesIO:
        """Gherkin senaryolarını .feature dosyasına yazar.

        Dosya başına '# Generated by AutoReq | {timestamp}' header'ı eklenir.
        Senaryolar '\\n---\\n' ayırıcısıyla birleştirilir.

        Args:
            scenarios: generate() metodundan dönen Gherkin blok listesi.
            output_path: Yazılacak dosya yolu. None ise
                         outputs/generated/scenarios.feature kullanılır.
            in_memory: True ise dosyayı disk yerine bellekte (io.BytesIO) üretir.

        Returns:
            Path | io.BytesIO: Yazılan .feature dosyasının mutlak yolu veya bellek tamponu.
        """
        import io

        timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
        header = f"# Generated by AutoReq | {timestamp}\n\n"
        content = header + "\n# ---\n\n".join(scenarios)

        if in_memory:
            buf = io.BytesIO(content.encode("utf-8"))
            buf.seek(0)
            _log.info("BDD .feature dosyası bellekte (BytesIO) oluşturuldu.")
            return buf

        if output_path is None:
            _GENERATED_DIR.mkdir(parents=True, exist_ok=True)
            output_path = _GENERATED_DIR / "scenarios.feature"
        else:
            output_path.parent.mkdir(parents=True, exist_ok=True)

        output_path.write_text(content, encoding="utf-8")
        _log.info("BDD .feature dosyası yazıldı | path={} scenarios={}", output_path, len(scenarios))
        return output_path
